#include "Utility.h"

#include <libgen.h>

std::string CUtility::dirName(const std::string &path)
{
    auto slashPos = path.rfind('/', -1);
    if (slashPos == std::string::npos) {
        return { "" };
    }
    return path.substr(0, slashPos);
}

bool CUtility::isPathRelative(const std::string &path)
{
    return path[0] != '/';
}
